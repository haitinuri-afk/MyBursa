async function main() {
  if (!process.env.MONGODB_URI) { console.error('חסר MONGODB_URI'); process.exit(1); }

  const client = new MongoClient(process.env.MONGODB_URI);
  await client.connect();
  console.log('✓ מחובר ל-MongoDB Atlas');

  const col = client.db('bursa').collection('company_profiles');

  // Keyword index עם weights לשמות
  await col.createIndex({ name: 1 }, { unique: true });
  await col.createIndex(
    { name: 'text', aliases: 'text', description: 'text', sector: 'text', drivers: 'text' },
    { default_language: 'none', weights: { name: 10, aliases: 8, sector: 5, drivers: 3, description: 1 } }
  );

  const entries = Object.entries(PROFILES);
  console.log(`\n📊 מעבד ${entries.length} פרופילי חברות...\n`);

  for (const [name, profile] of entries) {
    const text      = buildProfileText(name, profile);
    const embedding = await embed(text);

    await col.updateOne(
      { name },
      { $set: { name, ...profile, text, embedding, updatedAt: new Date() } },
      { upsert: true }
    );
    process.stdout.write(`  ✓ ${name.padEnd(18)} [${embedding.length}d]\n`);
  }

  await client.close();
  console.log(`\n✅ סיום — ${entries.length} פרופילים ב-MongoDB`);
  console.log('⚠  צור את ה-Vector Index ב-Atlas UI לפי scripts/atlas-vector-index.json');
}

main().catch(e => { console.error(e); process.exit(1); });
