async function seed() {
    let added = 0, skipped = 0;
    for (const chunk of chunks) {
        try {
            const res  = await fetch(`${BASE}/api/rag`, {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify(chunk),
            });
            const data = await res.json();
            if (data.ok) { added++; process.stdout.write('.'); }
            else { skipped++; process.stdout.write('s'); }
        } catch(e) {
            console.error('\nשגיאה:', e.message);
            skipped++;
        }
    }
    console.log(`\n\nסיום: ${added} chunks נוספו, ${skipped} דולגו (כבר קיימים).`);
}

seed();
